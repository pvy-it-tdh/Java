
public class Java12 {
	public static void main(String[] args) {
		int a=3;
		boolean b= false;
		System.out.println("+a= "+(+a));
		System.out.println("!b ="+!b);
		System.out.println("------------------");
		System.out.println("a= "+a);
		System.out.println("++a= "+ (++a));
		System.out.println("a++= "+ (a++));
		System.out.println("a="+a);
		System.out.println("--a= "+ (--a));
		System.out.println("a--= "+ (a--));
		System.out.println("a= "+ (a));
	}
}
